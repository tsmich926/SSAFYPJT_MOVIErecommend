<template>
  <div class="">
      <div class="">
          <div class="card" style="width: 18rem;" @mouseenter="zoomInCard" @mouseleave="zoomOutCard">
              <img :src="`https://image.tmdb.org/t/p/w500/${CARDhuman.profile_path}`" class="card-img-top" alt="Actor Image">
              <div class="card-body">
              <h5 class="card-name">{{CARDhuman.name }}</h5>
              <button 
            type="button" 
            @click="likeHuman()"
            class="btn btn-outline-primary"
          >
          {{ liked ? '❤': '🤍'}}
          </button>
              </div>
          </div>
      </div>
  </div>
</template>


<script>
export default {
name:'HumanCard',
data() {
return {
  liked: false
}
},
methods: {

  // toggleLike(actor) {
  //좋아요 로직---
  // },
  zoomInCard(event) {
    // 카드 확대하는 로직 구현
  event.target.classList.add('zoom-in');
  },
  zoomOutCard(event) {
    // 카드 축소하는 로직 구현
  event.target.classList.remove('zoom-in');

},

  likeHuman() {
    // 좋아요 로직 구현
    this.liked = !this.liked;
  }

},
props:{
CARDhuman:Object
  },

}
</script>


